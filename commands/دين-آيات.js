export default {
    name: 'اية',
    aliases: ['آية', 'قرآن', 'ايه'],
    category: 'ق5',
    description: 'إرسال آية قرآنية عشوائية من القائمة',
    execute: async (sock, m, args, ctx) => {
        const chatJid = m.key.remoteJid;

        // قائمة الآيات القرآنية
        const ayahs = [
            "﴿وَما أُبَرِّئُ نَفسي إِنَّ النَّفسَ لَأَمّارَةٌ بِالسّوءِ إِلّا ما رَحِمَ رَبّي إِنَّ رَبّي غَفورٌ رَحيمٌ﴾ [يوسف: ٥٣]",
            "﴿قُل مَن بِيَدِهِ مَلَكوتُ كُلِّ شَيءٍ وَهُوَ يُجيرُ وَلا يُجارُ عَلَيهِ إِن كُنتُم تَعلَمونَ﴾ [المؤمنون: ٨٨]",
            "﴿وَهُوَ الَّذي أَنشَأَ لَكُمُ السَّمعَ وَالأَبصارَ وَالأَبفِئدَةَ قَليلًا ما تَشكُرونَ﴾ [المؤمنون: ٧٨]",
            "﴿أَم تَسأَلُهُم خَرجًا فَخَراجُ رَبِّكَ خَيرٌ وَهُوَ خَيرُ الرّازِقينَ﴾ [المؤمنون: ٧٢]",
            "﴿وَلا نُكَلِّفُ نَفسًا إِلّا وُسعَها وَلَدَينا كِتابٌ يَنطِقُ بِالحَقِّ وَهُم لا يُظلَمونَ﴾ [المؤمنون: ٦٢]",
            "﴿وَكَذلِكَ مَكَّنّا لِيوسُفَ فِي الأَرضِ يَتَبَوَّأُ مِنها حَيثُ يَشاءُ نُصيبُ بِرَحمَتِنا مَن نَشاءُ وَلا نُضيعُ أَجرَ المُحسِنينَ﴾ [يوسف: ٥٦]",
            "﴿وَما عَلَينا إِلَّا البَلاغُ المُبينُ﴾ [يس: ١٧]",
            "{ الرَّحْمَـٰنُ (1) عَلَّمَ الْقُرْآنَ (2) خَلَقَ الْإِنسَانَ (3) عَلَّمَهُ الْبَيَانَ (4) الشَّمْسُ وَالْقَمَرُ بِحُسْبَانٍ (5) وَالنَّجْمُ وَالشَّجَرُ يَسْجُدَانِ (6) وَالسَّمَاءَ رَفَعَهَا وَوَضَعَ الْمِيزَانَ (7) أَلَّا تَطْغَوْا فِي الْمِيزَانِ (8) وَأَقِيمُوا الْوَزْنَ بِالْقِسْطِ وَلَا تُخْسِرُوا الْمِيزَانَ (9) وَالْأَرْضَ وَضَعَهَا لِلْأَنَامِ (10) } [سورة الرحمن]",
            "{ هَلْ أَتَىٰ عَلَى الْإِنسَانِ حِينٌ مِّنَ الدَّهْرِ لَمْ يَكُن شَيْئًا مَّذْكُورًا (1) إِنَّا خَلَقْنَا الْإِنسَانَ مِن نُّطْفَةٍ أَمْشَاجٍ نَّبْتَلِيهِ فَجَعَلْنَاهُ سَمِيعًا بَصِيرًا (2) إِنَّا هَدَيْنَاهُ السَّبِيلَ إِمَّا شَاكِرًا وَإِمَّا كَفُورًا (3) } [سورة الإنسان]"
        ];

        // اختيار آية عشوائية
        const randomAyah = ayahs[Math.floor(Math.random() * ayahs.length)];

        // تنسيق الرسالة بستايل ALPHA X
        const ayahMsg = `> ━ ╼╃ ⌬〔 ~𝐀𝐋𝐏𝐇𝐀 𝐗 𝐁𝐎𝐓~ 〕⌬ ╄╾ ━\n\n` +
                        `📖 *قال الله تعالى:*\n\n` +
                        `"${randomAyah}"\n\n` +
                        `> *┤────────────···*\n` +
                        `> 𝙱𝙰┇𝙰𝙻𝙿𝙷𝙰 𝚇 𝙱𝙾𝚃`;

        // التفاعل برمز الكتاب
        await sock.sendMessage(chatJid, { react: { text: '📖', key: m.key } });

        // إرسال الآية
        await sock.sendMessage(chatJid, { text: ayahMsg }, { quoted: m });
    }
};
